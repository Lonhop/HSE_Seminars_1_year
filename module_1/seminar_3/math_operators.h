#ifndef C_MATH_OPERATORS_H
#define C_MATH_OPERATORS_H

#include <iostream>

inline int plus(int a, int b) {
    return a + b;
}

inline int minus(int a, int b) {
    return a - b;
}

inline int multiply(int a, int b) {
    return a * b;
}

inline int divide(int a, int b) {
    return a / b;
}

#endif //C_MATH_OPERATORS_H